number_picked=raw_input("pick a number?")

print("The number picked is 1?")
print(int(number_picked) == 1)

print("The number picked is greater than 1?")
print(int(number_picked) > 1)

print("The number picked is less than or equal to 1?")
print(int(number_picked) <= 1)

print("The number picked is not equal to 1?")
print(int(number_picked) != 1)

